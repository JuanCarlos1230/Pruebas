public class Operaciones{
    public static void main(String[]args){
        int numUno = 4;
        int numDos = 4;
        int resultado = 0;
        
        resultado = numUno + numDos / 2;
        System.out.println("El resultado incorrecto és: " + resultado);
    
        resultado = (numUno + numDos) / 2;
        System.out.println("El resultado correcto és: " + resultado); 
    }
}
