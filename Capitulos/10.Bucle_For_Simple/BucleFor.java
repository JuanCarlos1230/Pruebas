// Programa que forma un bucle imprrimiendo 1,2,3,4,5
  
public class BucleFor{
    public static void main(String[] args){        
        for(int i = 1; i <= 5; i++){
            if (i >=1 && i <= 4){
                System.out.print(i + ",");
            }
            if (i == 5){
                System.out.println(i);
            }
        }            
    }
}

